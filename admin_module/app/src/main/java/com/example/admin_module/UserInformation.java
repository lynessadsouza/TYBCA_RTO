package com.example.admin_module;

public class UserInformation {

    String dob;
    String email;
    String id;
    String name;
    String password;
    String phone;
    String username;

    public String getDob() {
        return dob;
    }

    public String getEmail() {
        return email;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getPassword() {
        return password;
    }

    public String getPhone() {
        return phone;
    }

    public String getUsername() {
        return username;
    }
}
